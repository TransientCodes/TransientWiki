export const SITEMAP_PAGES = WIKI_PAGE_META;

const PAGE_OVERRIDES = Object.fromEntries(
  WIKI_PAGE_META.map((page) => [page.path, page]),
);

function stripMarkdown(markdown = '') {
  return markdown
    .replace(/```[\s\S]*?```/g, ' ')
    .replace(/`([^`]+)`/g, '$1')
    .replace(/!\[[^\]]*]\([^)]*\)/g, ' ')
    .replace(/\[([^\]]+)]\([^)]*\)/g, '$1')
    .replace(/<[^>]+>/g, ' ')
    .replace(/^#{1,6}\s+/gm, '')
    .replace(/[*_~>]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function firstHeading(markdown = '', fallback = SITE_NAME) {
  const match = markdown.match(/^#\s+(.+)$/m);
  if (match?.[1]?.trim()) {
    return match[1].trim();
  }

  const htmlHeadingMatch = markdown.match(/<h1[^>]*>(.*?)<\/h1>/i);
  return htmlHeadingMatch?.[1]?.trim() || fallback;
}

function firstParagraph(markdown = '') {
  const cleaned = markdown
    .split(/\n{2,}/)
    .map((block) => stripMarkdown(block))
    .find((block) => block.length > 110);

  return cleaned || DEFAULT_DESCRIPTION;
}

function truncate(value, maxLength = 165) {
  if (value.length <= maxLength) {
    return value;
  }

  return `${value.slice(0, maxLength - 1).trimEnd()}…`;
}

export function getPageMetadata({ content = '', entry = null, isNotFound = false, pathname = '/' }) {
  if (isNotFound) {
    return {
      canonical: `${SITE_URL}${pathname === '/' ? '/' : pathname}`,
      description:
        'Die angeforderte Wiki-Seite wurde nicht gefunden. Nutze die Startseite oder die Suche, um passende Inhalte zu Jobs, Quests, Citybuild und weiteren Server-Systemen zu finden.',
      robots: 'noindex,follow',
      title: `Seite nicht gefunden | ${SITE_NAME}`,
      type: 'website',
    };
  }

  const override = PAGE_OVERRIDES[pathname] ?? null;
  const fallbackTitle = entry?.isHome
    ? DEFAULT_TITLE
    : `${firstHeading(content, entry?.title || SITE_NAME)} | ${SITE_NAME}`;

  return {
    canonical: `${SITE_URL}${pathname === '/' ? '/' : pathname}`,
    description: override?.description || truncate(firstParagraph(content)),
    h1: override?.h1 || firstHeading(content, entry?.title || SITE_NAME),
    robots: 'index,follow,max-image-preview:large,max-snippet:-1,max-video-preview:-1',
    title: override?.title || fallbackTitle,
    type: override?.type || (entry?.isHome ? 'website' : 'article'),
  };
}

// FAQ-Markdown (## Frage + Antwort-Absätze) in FAQPage-mainEntity übersetzen
function parseFaqEntities(content) {
  const sections = content.split(/^## +/m).slice(1);
  return sections
    .map((section) => {
      const [questionLine, ...rest] = section.split('\n');
      const answer = rest
        .join(' ')
        .replace(/\[([^\]]*)\]\([^)]*\)/g, '$1')
        .replace(/[*_`]+/g, '')
        .replace(/\s+/g, ' ')
        .trim();
      return {
        '@type': 'Question',
        name: questionLine.trim(),
        acceptedAnswer: { '@type': 'Answer', text: answer },
      };
    })
    .filter((item) => item.name && item.acceptedAnswer.text);
}

export function getJsonLd(meta, { entry = null, content = '' } = {}) {
  const publisher = { '@type': 'Organization', name: 'TransientRealm' };
  const website = { '@type': 'WebSite', name: SITE_NAME, url: `${SITE_URL}/` };
  const graph = [];

  if (meta.type === 'website') {
    graph.push({
      '@type': 'WebSite',
      name: SITE_NAME,
      headline: meta.title,
      url: meta.canonical,
      inLanguage: 'de-DE',
      description: meta.description,
      publisher,
    });
  } else {
    graph.push({
      '@type': 'Article',
      headline: meta.h1,
      name: meta.h1,
      url: meta.canonical,
      inLanguage: 'de-DE',
      description: meta.description,
      image: DEFAULT_IMAGE,
      isPartOf: website,
      publisher,
    });
  }

  // Brotkrumen für verschachtelte Seiten (Startseite → Kategorie → Seite)
  if (entry?.isNested) {
    graph.push({
      '@type': 'BreadcrumbList',
      itemListElement: [
        { '@type': 'ListItem', position: 1, name: 'Startseite', item: `${SITE_URL}/` },
        { '@type': 'ListItem', position: 2, name: entry.labels[0] },
        { '@type': 'ListItem', position: 3, name: meta.h1, item: meta.canonical },
      ],
    });
  }

  if (entry?.route === '/wiki/faq' && content) {
    const mainEntity = parseFaqEntities(content);
    if (mainEntity.length) {
      graph.push({ '@type': 'FAQPage', mainEntity });
    }
  }

  return { '@context': 'https://schema.org', '@graph': graph };
}
